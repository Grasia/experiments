            <p>
                The project deals with how to consult citizens about things that have to do with the city
                in contexts where the opinion of citizens is needed. For example, a modification in the urban
                layout is proposed and there are several technically feasible possibilities to carry it out.
                The expected result of the project is twofold: first, if the answer to questions is more credible
                because a greater understanding of the problem is achieved by the user, we will ask for concrete
                modifications or we will ask for the opinions of citizens on what to do in different situations;
                second, an infrastructure that allows industry to create smart city solutions in a more efficient
                and less expensive way, facilitating experimentation, with the ability to run test batteries
                deterministically. This development philosophy is what we call Virtual Living Lab.
                The hypothesis of the project is that the novel serves as a narrative that captures the user's
                attention and makes them generate higher quality responses.
            </p>
